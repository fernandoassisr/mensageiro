<h2>Ansible</h2>
<br><br>
Ansible é uma linguagem de configuração para automatização e gerenciamento de configuração de sistemas. Como instalação de programas e outras tarefas envolvendo CD/CI, entrega continua/ integração continua.
<br>
Nesse documento abordaremos a ferramenta apenas no modo de salvar arquivos de configurações de sistemas. Criar modelos de instalação de novas máquinas, configurações de instalação do servidores. Criar arquivos de configuração de kubernetes e outros serviços a forma de execução do Ansible.
<hr>
